  LED_Init();
  LED_SetStatus(STATUS_IDLE_WRITE);

  while (1) { // WRITE loop

    status = Logger_WriteLoop();
    if (Logger_IsMemoryFull()) {
      status = STATUS_FULL;
    }
    LED_SetStatus(status);

    if (isKeyPressed(1000)) {
      break;
    }
  }

  Logger_Stop();
  LED_SetStatus(STATUS_IDLE_READ);
  LED_Blink(3);
  while (!isKeyUnpressed(200)) {};
  CLI_Start();

  while (1) { // READ loop

    enum cli_cmd_t cmd;
    if (CLI_GetCommand(&cmd)) {
      LED_SetStatus(STATUS_BUSY);

      switch (cmd) {
      case CMD_READ:
        Logger_Read(0);
        break;
      case CMD_DUMP:
        Logger_Read(1);
        break;
      case CMD_ERASE:
        Logger_Erase();
        break;
      case CMD_INFO:
        Logger_SendInfo();
        break;
      default:
        break;
      }
      LED_SetStatus(STATUS_IDLE_READ);
    }

    if (isKeyPressed(3000)) {
      break;
    }
  }

  LED_SetStatus(STATUS_BUSY);
  Logger_Erase();
